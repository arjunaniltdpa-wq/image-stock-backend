export function generateSEOFromFilename(filename) {
  const words = tokenize(filename);
  const { primary, secondary } = detectCategory(words);

  return {
    title: makeTitle(words, primary, secondary),
    description: makeDescription(words, primary, secondary),
    alt: makeAlt(words, primary),
    tags: makeTags(words, primary, secondary),
    keywords: makeKeywords(words, primary, secondary),
    category: primary,
    secondaryCategory: secondary
  };
}