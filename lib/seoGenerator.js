function makeTitle(words, primary, secondary) {
  const main = words.join(" ");
  if (secondary)
    return `${main}  Free ${primary} & ${secondary} HD Images | Pixeora`;
  return `${main}  Free ${primary} HD Images | Pixeora`;
}

