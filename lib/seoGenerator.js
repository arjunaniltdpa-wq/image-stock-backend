function tokenize(filename) {
  return filename
    .replace(/\.[^/.]+$/, "")
    .toLowerCase()
    .replace(/[_]+/g, "-")
    .replace(/[.,()]/g, " ")
    .replace(/\s+/g, "-")
    .split("-")
    .map(w => w.trim())
    .filter(Boolean);
}

// -------------------------------------------------------------
//  DETECT CATEGORY (Primary + Secondary)
// -------------------------------------------------------------

function detectCategory(words) {
  const scores = {};

  // Initial score
  Object.keys(CATEGORY_KEYWORDS).forEach(cat => (scores[cat] = 0));

  // Weighted scoring
  for (const word of words) {
    for (const [cat, data] of Object.entries(CATEGORY_KEYWORDS)) {
      if (data.keywords.includes(word)) {
        scores[cat] += data.weight + 5; // boost for exact match
      }
    }
    if (categoryMap[word]) scores[categoryMap[word]] += 50;
  }

  // Sort categories by score
  const sorted = Object.entries(scores).sort((a, b) => b[1] - a[1]);

  const primary = sorted[0][0];
  const secondary = sorted[1] && sorted[1][1] > 0 ? sorted[1][0] : null;

  return { primary, secondary };
}

